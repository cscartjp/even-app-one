/**
 * ▼▼▼ グルメ店データ 記入テンプレート ▼▼▼
 *
 * 【使い方】
 *   1. 下の `shops` 配列に自分の店を追記する（{ ... } のブロックを増やすだけ）
 *   2. `defaultOrigin` を、GPS が取れないときの距離の基準点に書き換える
 *   3. 書き終わったら、このファイルの中身を src/data/shops.ts に上書きコピー
 *      （または、このファイルを shops.ts にリネームして元のを置き換え）
 *
 * 【記入のヒント・制約】
 *   - name … 店名。リスト1行は64字までなので短めに（必須）
 *   - genre … ジャンル。これがそのまま「ジャンル選択」の選択肢になる（必須）
 *             ※ 同じジャンルは表記を揃える（'ラーメン' と 'らーめん' は別扱いになる）
 *   - area … エリア/最寄り駅など。詳細に表示（任意・不要なら行ごと削除）
 *   - lat/lon … 緯度・経度（必須）。Googleマップで地点を右クリック→座標をコピー
 *   - weekday/weekend … 営業帯。r(開時,開分, 閉時,閉分) で書く
 *       ・通し営業       → [r(11, 0, 22, 0)]
 *       ・昼夜の2部制     → [r(11, 0, 14, 30), r(17, 30, 21, 0)]
 *       ・その曜日が定休  → []   (空配列)
 *       ・深夜またぎは未対応。閉店は当日24:00まで → r(..., 24, 0)
 *   - note … 一言メモ。「現金のみ」等（任意・不要なら行ごと削除）
 */
import type { GeoPoint, Shop, TimeRange } from './shops'

/** r(開時, 開分, 閉時, 閉分) → 営業帯1つ */
const r = (
  openH: number,
  openM: number,
  closeH: number,
  closeM: number,
): TimeRange => ({ open: openH * 60 + openM, close: closeH * 60 + closeM })

/**
 * GPS が取れなかったときの、グルメ距離計算の基準点。
 * （電車は別途デフォルト大保駅。ここはグルメ専用）
 */
